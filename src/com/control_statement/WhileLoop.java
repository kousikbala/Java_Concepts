package com.control_statement;

public class WhileLoop {
	
	public static void main(String[] args) {
		
		int i=1;
		
		while (i<=5) {
			
		System.out.println(1);
		
		     i++; 
			
			
		}
		
		
		int j=1;
		
		while (j<=7) {
			
			System.out.println("*");
			
			j++;
		}
		
		int k= 1;
				
		while (k<=5) {
			
			System.out.println(1);
			System.out.println(2);
			
			k++;
		}
		
		
	}

}
