package com.control_statement;

public class DoWhileLoop {
	
	public static void main(String[] args) {
		
		int i = 1;
		do {
			
			System.out.println(1);
			
			i++;
			
		} while (i<=5);
		
		
		
		
		int j =1;
		
		do {
			
			System.out.println("*");
			
			j++;
			
		} while (j<=5);
		
		
		
		
		int k=1;
		do {
			
			System.out.println(1);
			System.out.println(2);
			
			k++;
			
		} while (k<=5);
		
		
		
		 
		}
		
		
			
	}
	


