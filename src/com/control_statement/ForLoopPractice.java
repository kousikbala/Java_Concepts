package com.control_statement;

public class ForLoopPractice {
	
	public static void main(String[] args) {
		
		
		
		for(int a=1; a<=5; a++) {
			
			System.out.println(1);
		}
		
		
		
		
		for (int b=1; b<=6; b++) {
			
			System.out.println("*");
		}
		
		
		
		
		for (int c=1; c<=5; c++) {
			
			System.out.println(1);
			System.out.println(2);
		}
		
	}

}
